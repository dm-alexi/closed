#include <stdlib.h>

int		ft_strncmp(const char *s1, const char *s2, size_t num)
{
	if (s1 == s2)
		return (0);
	while (num && *s1 && *s1 == *s2)
	{
		--num;
		++s1;
		++s2;
	}
	return (!num ? 0 : *s1 - *s2);
}
