#include <stdlib.h>

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (n && *s1 && *s1 == *s2)
	{
		--n;
		++s1;
		++s2;
	}
	return (!n ? 0 : *s1 - *s2);
}
